CREATE TABLE "airport" (
   "ident"   				VARCHAR(7)		NOT NULL,
   "type"                   VARCHAR(32)		NOT NULL,
   "name"                   VARCHAR(128)		NOT NULL,
   "elevation"         		INT             ,
   "continent"              VARCHAR(2)		NOT NULL,
   "country"                VARCHAR(2)		NOT NULL,
   "region"                 VARCHAR(7)		NOT NULL
);